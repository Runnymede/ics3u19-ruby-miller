/**Hello.java <p>
 * This program will print "Hello" 5 times, each on a new line, using a for loop <p>
 * November 11, 2019
 * @author Ruby Miller
 */

public class Hello {

	/**
	 * Entry point to this program
	 * @param args unused
	 */

	public static void main(String[] args) {

		for (int i = 0; i <10; i = i+1) //This line declares, assigns, checks, and changes the variable i, the loop will stop after 5 repeats
		{
			System.out.println("Hello");
		}
		
	}

}
